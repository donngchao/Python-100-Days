"""
求解《百钱百鸡》问题
1只公鸡5元 1只母鸡3元 3只小鸡1元 用100元买100只鸡
问公鸡 母鸡 小鸡各有多少只

Version: 0.1
Author: 骆昊
Date: 2018-03-02
"""

for x in range(0, 20):#最多买20只公鸡
    for y in range(0, 33):#最多买33只母鸡
        z = 100 - x - y#总的数目100减去公鸡和母鸡等于小鸡的数目
        if 5 * x + 3 * y + z / 3 == 100:#总的金额不变 --> 100
            print('公鸡: %d只, 母鸡: %d只, 小鸡: %d只' % (x, y, z))

